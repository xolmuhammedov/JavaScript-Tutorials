// Math operators in JavaScript
Math.abs(x); // Sondan murakkab qiymatni olib tashlaydi.
Math.ceil(x); // Berilgan sonni yuqori butun soniga yaqin eng katta butun soniga olib ko'taradi.
Math.floor(x); // Berilgan sonni past butun soniga yaqin eng kichik butun soniga olib ko'taradi.
Math.random(); // 0 va 1 oralig'ida tasodifiy sonni qaytaradi.
Math.max(x, y); // Berilgan sonlarning eng katta qiymatini qaytaradi.
Math.min(x, y); // Berilgan sonlarning eng kichik qiymatini qaytaradi.
Math.pow(x, y); // x ning y-darajasini hisoblaydi.
Math.sqrt(x); // Berilgan sonning ildizini hisoblaydi.
Math.round(x); // Berilgan sonni yaqin butun soniga yaxlitlab chiqaradi.
Math.sin(x), Math.cos(x), Math.tan(x); // Berilgan burchning sinusini, kosinusini va tangentini hisoblaydi (x radian cinsidagi).
Math.log(x); // Berilgan sonning natural logarifmini qaytaradi.
Math.exp(x); // e darajasini hisoblaydi (e ≈ 2.718).
Math.PI; // π ni ta'minlaydi.

console.log(Math.abs(-5)); // 5
console.log(Math.ceil(4.3)); // 5
console.log(Math.floor(4.7)); // 4
console.log(Math.max(10, 20, 30)); // 30
console.log(Math.min(10, 20, 30)); // 10
console.log(Math.pow(2, 3)); // 8
console.log(Math.sqrt(16)); // 4
console.log(Math.round(4.5)); // 5
console.log(Math.random()); // Tasodifiy son
console.log(Math.sin(Math.PI / 2)); // 1 (π/2 sinusi 1 ga teng)
console.log(Math.log(Math.E)); // 1 (e ni natural logarifmi)
console.log(Math.exp(1)); // e (e darajasi)
console.log(Math.PI); // π

